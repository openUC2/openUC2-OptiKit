import { useEffect, useCallback } from 'react';
import {
  Box,
  Stepper,
  Step,
  StepLabel,
  StepButton,
  Button,
  Typography,
  Paper,
  Chip,
  Tooltip,
  CircularProgress,
} from '@mui/material';
import {
  ArrowBack,
  ArrowForward,
  ShoppingCart,
  Refresh,
} from '@mui/icons-material';
import { useNavigate } from 'react-router-dom';
import { useFrameWizardStore } from '../stores/frameWizardStore';
import { loadAllLibraries } from '../utils/frameLibraryLoader';
import { LightSourceStep } from './frameWizard/LightSourceStep';
import { AutofocusStep } from './frameWizard/AutofocusStep';
import { ObjectiveStep } from './frameWizard/ObjectiveStep';
import { SampleHolderStep } from './frameWizard/SampleHolderStep';
import { RevolverStep } from './frameWizard/RevolverStep';
import { OverviewCameraStep } from './frameWizard/OverviewCameraStep';
import { FluorescenceStep } from './frameWizard/FluorescenceStep';
import { CameraStep } from './frameWizard/CameraStep';
import { TubeLensStep } from './frameWizard/TubeLensStep';
import { ControlInputsStep } from './frameWizard/ControlInputsStep';
import { CustomizationStep } from './frameWizard/CustomizationStep';
import { FramePreview } from './frameWizard/FramePreview';

const STEP_LABELS = [
  'Light Source',
  'Autofocus',
  'Objectives',
  'Sample Holder',
  'Obj. Revolver',
  'Overview Cam',
  'Fluorescence',
  'Camera',
  'Tube Lens',
  'Controls',
  'Custom',
];

export function FrameWizardPage() {
  const navigate = useNavigate();
  const {
    currentStep,
    setStep,
    nextStep,
    prevStep,
    resetWizard,
    setObjectives,
    setLenses,
    setCameras,
    setFluorescenceOptions,
    getTotalPrice,
    getStepPrice,
    getSelectedComponents,
    isLoading,
    setIsLoading,
  } = useFrameWizardStore();

  useEffect(() => {
    setIsLoading(true);
    loadAllLibraries().then(({ objectives, lenses, cameras, fluorescence }) => {
      setObjectives(objectives);
      setLenses(lenses);
      setCameras(cameras);
      setFluorescenceOptions(fluorescence);
      setIsLoading(false);
    });
  }, [setObjectives, setLenses, setCameras, setFluorescenceOptions, setIsLoading]);

  const handleOpenInCanvas = useCallback(() => {
    const components = getSelectedComponents();
    // Build a layout JSON compatible with the main editor
    const layout = {
      uc2_components: components.map((c, i) => ({
        name: `${c.moduleId}_${i}`,
        file: '',
        grid_pos: c.gridPos,
        rotation: c.rotation,
        moduleId: c.moduleId,
        originalName: c.moduleId,
        description: '',
        params: c.params || {},
      })),
      annotations: [],
      layers: [{ id: 'layer-0', name: 'Layer 0', index: 0, visible: true }],
      name: 'FRAME Configuration',
      author: '',
      githubAccount: '',
      description: 'Generated by FRAME Wizard',
      category: 'Microscopy',
      screenshot: '',
      uc2_verified: false,
      version: '1.0.0',
      createdAt: new Date().toISOString(),
      collection: ['Microscopy', 'FRAME'],
      notification: '',
      metadata: {
        version: '1.0',
        created: new Date().toISOString(),
        software: 'OpenUC2 OptiKit FRAME Wizard',
      },
    };

    // Encode as base64 and navigate to canvas
    const encoded = btoa(JSON.stringify(layout));
    navigate(`/configurator?data=${encoded}`);
  }, [getSelectedComponents, navigate]);

  const stepPrice = getStepPrice(currentStep);
  const totalPrice = getTotalPrice();

  const renderStep = () => {
    switch (currentStep) {
      case 0: return <LightSourceStep />;
      case 1: return <AutofocusStep />;
      case 2: return <ObjectiveStep />;
      case 3: return <SampleHolderStep />;
      case 4: return <RevolverStep />;
      case 5: return <OverviewCameraStep />;
      case 6: return <FluorescenceStep />;
      case 7: return <CameraStep />;
      case 8: return <TubeLensStep />;
      case 9: return <ControlInputsStep />;
      case 10: return <CustomizationStep />;
      default: return null;
    }
  };

  if (isLoading) {
    return (
      <Box sx={{ display: 'flex', justifyContent: 'center', alignItems: 'center', height: '100vh' }}>
        <CircularProgress size={60} />
        <Typography sx={{ ml: 2 }} variant="h6">Loading FRAME libraries...</Typography>
      </Box>
    );
  }

  return (
    <Box sx={{ display: 'flex', flexDirection: 'column', height: '100vh', bgcolor: '#f5f5f5' }}>
      {/* Header */}
      <Paper
        elevation={2}
        sx={{
          p: 2,
          display: 'flex',
          alignItems: 'center',
          justifyContent: 'space-between',
          borderRadius: 0,
          bgcolor: '#1e4670',
          color: 'white',
        }}
      >
        <Box sx={{ display: 'flex', alignItems: 'center', gap: 2 }}>
          <img
            src="/configurator/icons/uc2-frame.svg"
            alt="UC2 FRAME"
            style={{ height: 40, filter: 'brightness(0) invert(1)' }}
          />
          <Box>
            <Typography variant="h5" fontWeight="bold">
              UC2 FRAME Configurator
            </Typography>
            <Typography variant="body2" sx={{ opacity: 0.8 }}>
              Build your custom microscope step by step
            </Typography>
          </Box>
        </Box>
        <Box sx={{ display: 'flex', alignItems: 'center', gap: 2 }}>
          <Chip
            icon={<ShoppingCart />}
            label={`Total: $${totalPrice.toLocaleString()}`}
            sx={{ bgcolor: '#fff', color: '#1e4670', fontWeight: 'bold', fontSize: '1rem', p: 1 }}
          />
          <Tooltip title="Reset wizard">
            <Button
              variant="outlined"
              onClick={resetWizard}
              sx={{ color: 'white', borderColor: 'rgba(255,255,255,0.5)' }}
              startIcon={<Refresh />}
              size="small"
            >
              Reset
            </Button>
          </Tooltip>
        </Box>
      </Paper>

      {/* Stepper */}
      <Paper elevation={1} sx={{ px: 2, py: 1, borderRadius: 0 }}>
        <Stepper nonLinear activeStep={currentStep} alternativeLabel>
          {STEP_LABELS.map((label, index) => (
            <Step key={label} completed={index < currentStep}>
              <StepButton onClick={() => setStep(index)}>
                <StepLabel>{label}</StepLabel>
              </StepButton>
            </Step>
          ))}
        </Stepper>
      </Paper>

      {/* Main Content Area */}
      <Box sx={{ flex: 1, display: 'flex', overflow: 'hidden', p: 2, gap: 2 }}>
        {/* Left: Wizard Step */}
        <Paper
          elevation={2}
          sx={{
            flex: 1,
            p: 3,
            overflow: 'auto',
            display: 'flex',
            flexDirection: 'column',
          }}
        >
          {/* Step price indicator */}
          {stepPrice > 0 && (
            <Chip
              label={`This step: +$${stepPrice.toLocaleString()}`}
              color="primary"
              variant="outlined"
              sx={{ alignSelf: 'flex-end', mb: 1 }}
            />
          )}
          {renderStep()}
        </Paper>

        {/* Right: Frame Preview */}
        <Paper elevation={2} sx={{ width: 400, minWidth: 350, overflow: 'auto' }}>
          <FramePreview />
        </Paper>
      </Box>

      {/* Footer Navigation */}
      <Paper
        elevation={3}
        sx={{
          p: 2,
          display: 'flex',
          justifyContent: 'space-between',
          alignItems: 'center',
          borderRadius: 0,
        }}
      >
        <Button
          variant="outlined"
          startIcon={<ArrowBack />}
          onClick={prevStep}
          disabled={currentStep === 0}
        >
          Previous
        </Button>
        <Typography variant="body2" color="text.secondary">
          Step {currentStep + 1} of {STEP_LABELS.length}
        </Typography>
        {currentStep === STEP_LABELS.length - 1 ? (
          <Button
            variant="contained"
            color="success"
            startIcon={<ShoppingCart />}
            onClick={handleOpenInCanvas}
          >
            Open in Editor
          </Button>
        ) : (
          <Button variant="contained" endIcon={<ArrowForward />} onClick={nextStep}>
            Next
          </Button>
        )}
      </Paper>
    </Box>
  );
}
